# En Ruby existen dos grupos de números: enteros y flotantes
# Los Floats contienen al menos un dígito después del punto
# Los números en ruby son objetos. Esto quiere decir que se pueden ejecutar métodos sobre ellos

# Pasar de un entero a un float
num = 10.to_f
puts num

# Pasar de un float a un entero
num = 10.to_i
puts num

